package cn.jianwoo.blog.config.router.admin;

import cn.jianwoo.blog.config.router.UrlConfig;

/**
 * @author GuLihua
 * @Description
 * @date 2020-11-25 14:40
 */
public class DynamicApiUrlConfig extends UrlConfig {
    public static final String URL_PREFIX = "/api/admin/dynamic";
    public static final String URL_VISIT_QUERY = "/visit/query";
    public static final String URL_COMMENT_QUERY = "/comment/query";


    /**
     * 获取默认的url
     *
     * @param url url
     * @return
     * @author gulihua
     */
    public static String getUrl(String url) {
        return getUrl(url, DEFAULT_VERSION);
    }

    /**
     * 获取指定版本的url
     *
     * @param url     url
     * @param version 版本
     * @return
     * @author gulihua
     */
    public static String getUrl(String url, int version) {
        return getUrl(URL_PREFIX, url, version);
    }
}
